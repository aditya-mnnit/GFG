class Solution {
  public:
    // Function to find the sum of contiguous subarray with maximum sum.
    int maxSubarraySum(vector<int> &a) {
        // code here...
        int s=0;
        int mx=-1e9;
        int n=a.size();
        for(int i=0;i<n;i++)
        {
            s+=a[i];
            mx=max(mx,s);
             if(s<0)
            s=0;
        }
        return mx;
    }
};

